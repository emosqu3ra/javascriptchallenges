// EX OF PRIMITIVE DATA TYPES

// LET VARIABLE CAN BE REASSIGNED IN VALUE 
let hello = 'my name is steph';
console.log(hello);

// CONST VARIABLE CANNOT BE REASSIGNED
const age = 'I am 30';
console.log(hello, age);

// EX OF NON PRIMITIVE DATA TYPES (ARRAY AND OBJECTS)
let seasons =['Winter', 'Spring', 'Summer', 'Fall'];
console.log(seasons[3]);

